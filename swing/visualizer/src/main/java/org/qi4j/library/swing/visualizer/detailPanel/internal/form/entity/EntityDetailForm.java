    public final void updateModel( EntityDetailDescriptor aDescriptor )
    {
        entityIds.removeAllItems();

        if( aDescriptor != null )
        {
            ModuleDetailDescriptor moduleDetailDescriptor = aDescriptor.module();
            String moduleName = moduleDetailDescriptor.descriptor().name();
            String layerName = moduleDetailDescriptor.layer().descriptor().name();

            Module module = application.findModule( layerName, moduleName );
            UnitOfWorkFactory uowf = module.unitOfWorkFactory();
            UnitOfWork uow = uowf.nestedUnitOfWork();

            try
            {
                QueryBuilderFactory qbf = uow.queryBuilderFactory();
                QueryBuilder<?> queryBuilder = qbf.newQueryBuilder( aDescriptor.descriptor().type() );
                Query<EntityComposite> query = (Query<EntityComposite>) queryBuilder.newQuery();

                for( EntityComposite entity : query )
                {
                    String entityIdentity = entity.identity().get();
                    entityIds.addItem( entityIdentity );
                }
            }
            finally
            {
                uow.discard();
            }
        }
    }

    private void createUIComponents()
    {
        DefaultComponentFactory cmpFactory = DefaultComponentFactory.getInstance();
        // TODO: Localization
        listSeparator = cmpFactory.createSeparator( "Detail" );
    }

